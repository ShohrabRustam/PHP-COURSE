<h2>PHP Constants</h2>
<p>Constants are like variables except that once they are defined they cannot be changed or undefined.</p>
<h2>PHP Constants</h2>
<p>A constant is an identifier (name) for a simple value. The value cannot be changed during the script.</p>
<p>A valid constant name starts with a letter or underscore (no $ sign before the constant name).</p>
<p><b>Note:</b>Unlike variables, constants are automatically global across the entire script.</p>
<h2>Create a PHP Constant</h2>
<p>To create a constant, use the <b>define()</b> function</p>
<h5>Syntax</h5>
<p><b>define(name, value, case-insensitive)</b></p>
<h6>Parameters:</h6>
<ul>
    <li>name: Specifies the name of the constant</li>
    <li>value: Specifies the value of the constant</li>
</ul>
